# Запрос имени студента
name = input("Введите имя студента: ")

# Запрос оценки студента
grade = input("Введите оценку студента (от 1 до 12): ")

# Проверка, является ли оценка числом
if not grade.isdigit():
    print("Ошибка: Оценка должна быть целым числом.")
else:
    grade = int(grade)  # Преобразование строки в целое число

    # Определение уровня знаний
    if 1 <= grade <= 3:
        level = "Начальный уровень"
    elif 4 <= grade <= 6:
        level = "Средний уровень"
    elif 7 <= grade <= 9:
        level = "Достаточный уровень"
    elif 10 <= grade <= 12:
        level = "Высокий уровень"
    else:
        level = "Ошибка: Оценка должна быть от 1 до 12"  # Сообщение об ошибке для некорректной оценки

    # Вывод результата
    print(f"{name}, ваш уровень знаний: {level}")
