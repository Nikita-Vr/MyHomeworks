# Набор данных о фильмах
small_dict = {
    "Человек-муравей и Оса: Квантомания": 2023,
    "Стражи Галактики. Часть 3": 2023,
    "Капитан Марвел 2": 2023,
    "Дэдпул 3": 2024,
    "Капитан Америка: Дивный новый мир": 2024,
    "Громовержцы": 2024,
    "Блэйд": 2025,
    "Фантастическая четвёрка": 2025,
    "Мстители: Династия Канга": 2026,
    "Мстители: Секретные войны": 2027,
    "Безымянный фильм о Человеке-пауке": None,
    "Безымянный фильм о Шан-Чи": None,
    "Безымянный фильм о Вечных": None,
    "Безымянный фильм о мутантах": None,
}

# Список для хранения найденных фильмов
found_movies = []

# Поиск фильмов, вышедших после 2024 года
for movie, year in small_dict.items():
    if year is None:
        continue  # Пропускаем, если year - None
    if year > 2024:  # Проверка, что year больше 2024
        found_movies.append(movie.lower())  # Приведение названия к нижнему регистру

# Вывод найденных фильмов
if found_movies:
    print("Фильмы, вышедшие после 2024 года:", found_movies)
else:
    print("Фильмы не найдены.")
